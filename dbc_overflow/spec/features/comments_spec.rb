describe 'Comments' do
  describe 'adding a comment' do

    describe 'question comment' do
      context 'valid params' do
        it 'adds a comment for a question'
      end

      context 'invalid params' do
        it 'does not add a comment for a question'
      end
    end

    describe 'answer comment' do
      context 'valid params' do
        it 'adds a comment for an answer'
      end

      context 'invalid params' do
        it 'does not add a comment for an answer'
      end
    end
  end
end